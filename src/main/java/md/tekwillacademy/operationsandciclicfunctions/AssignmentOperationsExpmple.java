package md.tekwillacademy.operationsandciclicfunctions;

public class AssignmentOperationsExpmple {
    public static void main(String[] args) {
        int a = 65;
        int b = 40;

        int result = a;
        System.out.println(result);

        result += b;
        //result = calculated above result + b
        System.out.println(result);

        result -= a;
        System.out.println(result);

        result *= 5;
        // the above calculated result * 5
        System.out.println(result);

        result /= 10;
        System.out.println(result);

        result %=a;
        //
        System.out.println(result);


    }
}
